// Podemos realizar operaciones matematicas con los numeros

const numero = 30;
const numero2 = 20;
let resultado;

// Suma
resultado = numero + numero2;

// Resta
resultado = numero - numero2;

// Multiplicacion
resultado = numero * numero2;

// Division
resultado = numero / numero2;

// Modulo
resultado = numero % numero2;

console.log(resultado);