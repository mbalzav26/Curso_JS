// Incremento / decremento

let puntaje = 5;

// para incrementar o decrementar un valor de uno en uno

puntaje++; // nos retorna 5 en la primera llamada y 6 en la segunda
++puntaje; // nos retorna 6 en la primera llamada

puntaje--; // nos retorna 4 en la primera llamada y 3 en la segunda
--puntaje; // nos retorna 3 en la primera llamada

// Para incrementar o decrementar por mas de uno

puntaje +=3; // nos retorna 8
puntaje -=3; // nos retorna 2

console.log(puntaje);