/* Creacion de numeros con Javascript 

A diferencia de otros lenguajes en JS se deduce el tipo de numero */

const numero1 = 10;
const numero2 = 20.3;
const numero3 = -10;

console.log(numero1);
console.log(numero2);
console.log(numero3);

// Si el numero esta entre comillas se considera un string

// Esta forma no es muy comun en JS pero es utilizable

const numero4 = new Number(80);
console.log(numero4);
// la diferencia es que en la nos los muestra como un objeto de tipo number



