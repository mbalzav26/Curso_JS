// comparadores strictos en java

let puntaje = 1000;

// comparador diferente

if(puntaje != 1000){
    console.log("Ganaste");
}else{
    console.log("Perdiste");
}

// != comparador no estricto
// !== comparador estricto

// comparador estricto de diferente

if(puntaje !== 1000){
    console.log("Ganaste");
}else{
    console.log("Perdiste");
}

// comparador de igualdad

if(puntaje == 1000){
    console.log("Ganaste");
}else{
    console.log("Perdiste");
}

// comparador de igualdad estricto

if(puntaje === 1000){
    console.log("Ganaste");
}else{
    console.log("Perdiste");
}

